    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String serial = txtSerial.getText();
        String title = txtTitle.getText();
        String author = txtAuthor.getText();
        String cat = cmbCat.getSelectedItem().toString();
        String publisher = txtPub.getText();
        String year = txtYear.getText();
        String status = cmbStatus.getSelectedItem().toString();
        
        try{
        Connection myConn = conDB.getConnection();
            Statement stat = myConn.createStatement();
            String sqlInsert = "INSERT INTO books (serial, title, author, category, publisher, year, status, imageName, imageFile) VALUES (?,?,?,?,?,?,?,?,?)";
            
            if(txtTitle.getText().equals("")|| txtAuthor.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Invalid!");
                txtSerial.setText("");
                txtTitle.setText("");
                txtAuthor.setText("");
                cmbCat.setSelectedIndex(0);
                txtPub.setText("");
                txtYear.setText("");
                cmbStatus.setSelectedIndex(0);
                lblImage.setIcon(null);
            }
            //no image inserted
            else if(path == null){
                JOptionPane.showMessageDialog(rootPane, "Insert image!");
            }
            else if (path != null){
                File f = new File(path);
                
                PreparedStatement stmt = myConn.prepareStatement(sqlInsert);
                InputStream is = new FileInputStream(f);
                
                stmt.setString(1, serial);
                stmt.setString(2, title);
                stmt.setString(3, author);
                stmt.setString(4, cat);
                stmt.setString(5, publisher);
                stmt.setString(6, year);
                stmt.setString(7, status);
                stmt.setString(8, f.getName());
                stmt.setBlob(9, is);
                
                int rowsInserted = stmt.executeUpdate();
            
            if(rowsInserted > 0){
                JOptionPane.showMessageDialog(rootPane, "Added!");
                txtSerial.setText("");
                txtTitle.setText("");
                txtAuthor.setText("");
                cmbCat.setSelectedIndex(0);
                txtPub.setText("");
                txtYear.setText("");
                cmbStatus.setSelectedIndex(0);
                lblImage.setIcon(null);
                adminPanel.ShowData(); 
            }
            }    
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseActionPerformed
        // TODO add your handling code here:
        
        //opens file chooser to upload an image
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter fnwf = new FileNameExtensionFilter("PNG JPG AND JPEG", "png", "jpeg", "jpg");
        fileChooser.addChoosableFileFilter(fnwf);
        int load = fileChooser.showOpenDialog(null);
        
        if(load==fileChooser.APPROVE_OPTION){
            
            File f = fileChooser.getSelectedFile();
            
            path = f.getAbsolutePath();
            ImageIcon ii = new ImageIcon(path);
            Image img = ii.getImage().getScaledInstance(200,243, Image.SCALE_SMOOTH);
            lblImage.setIcon(new ImageIcon(img));
        }
    }//GEN-LAST:event_btnBrowseActionPerformed

