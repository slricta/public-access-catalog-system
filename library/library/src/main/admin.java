    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        
        String username = txtUser.getText();
        String password = String.valueOf(txtPass.getPassword());
        
        String sqlSELECT = "SELECT * FROM admin WHERE username=? and password=?";
        
        try{
            Connection myConn = conDB.getConnection();
            PreparedStatement ps = myConn.prepareStatement(sqlSELECT);
            
            ps.setString(1, username);
            ps.setString(2, password);
            
            ResultSet rs = ps.executeQuery();
            
            if(rs.next()){
                adminPanel admPanel = new adminPanel();
                admPanel.setVisible(true);
                admPanel.pack();
                admPanel.setLocationRelativeTo(null);
                admPanel.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Incorrect!");
            }
            
        }catch(Exception e){
            
        }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

